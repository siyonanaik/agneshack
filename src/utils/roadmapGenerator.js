/**
 * Get translation for a given language and key
 */
function t(language, key) {
  const langTranslations = TRANSLATIONS[language] || TRANSLATIONS['English'];
  return langTranslations[key] || TRANSLATIONS['English'][key] || key;
}

/**
 * Generate roadmap data from AI analysis findings
 * Creates structured data for visualization of health conditions and improvement plans
 */
export function generateRoadmapData(aiResponse, profile, language = 'English') {
  if (!aiResponse) return null;

  const findings = aiResponse.findings || [];
  const questions = aiResponse.questions || [];
  const summary = aiResponse.summary || '';
  const disclaimer = aiResponse.disclaimer || '';

  // Build the roadmap nodes
  const nodes = {
    id: 'root',
    label: t(language, 'healthOverview'),
    type: 'root',
    children: []
  };

  // Add current status node
  const statusNode = {
    id: 'status',
    label: t(language, 'currentStatus'),
    type: 'status',
    children: []
  };

  // Add findings as branches
  findings.forEach(finding => {
    const statusColor = finding.status === 'NORMAL' ? 'green' : 
                       finding.status === 'BORDERLINE' ? 'amber' : 
                       finding.status === 'HIGH' ? 'red' : 'blue';
    
    statusNode.children.push({
      id: `finding-${finding.item}`,
      label: finding.item,
      sublabel: finding.explanation || '',
      status: finding.status,
      color: statusColor,
      type: 'finding'
    });
  });

  nodes.children.push(statusNode);

  // Add action items node
  const actionNode = {
    id: 'actions',
    label: t(language, 'recommendedActions'),
    type: 'action',
    children: []
  };

  // Generate actions from findings
  findings.forEach(finding => {
    if (finding.status !== 'NORMAL') {
      actionNode.children.push({
        id: `action-${finding.item}`,
        label: `${t(language, 'followUpOn')} ${finding.item}`,
        priority: finding.status === 'HIGH' ? 'high' : 'medium',
        type: 'action-item'
      });
    }
  });

  // Add doctor questions as action items
  questions.forEach((q, idx) => {
    actionNode.children.push({
      id: `question-${idx}`,
      label: q,
      priority: 'medium',
      type: 'question'
    });
  });

  // Always add general health improvement actions
  actionNode.children.push({
    id: 'action-general',
    label: t(language, 'maintainHealthy'),
    priority: 'low',
    type: 'general'
  });

  actionNode.children.push({
    id: 'action-followup',
    label: t(language, 'scheduleCheckups'),
    priority: 'medium',
    type: 'general'
  });

  nodes.children.push(actionNode);

  // Add timeline node
  const timelineNode = {
    id: 'timeline',
    label: t(language, 'timeline'),
    type: 'timeline',
    children: [
      {
        id: 'timeline-immediate',
        label: t(language, 'thisWeek'),
        type: 'milestone',
        color: 'red',
        children: [
          { id: 'tl-1', label: t(language, 'reviewWithDoctor'), type: 'task' },
          ...(questions.length > 0 ? [{ 
            id: 'tl-2', 
            label: `${t(language, 'prepareQuestions')} (${questions.length})`, 
            type: 'task' 
          }] : [])
        ]
      },
      {
        id: 'timeline-short',
        label: t(language, 'weeks1to4'),
        type: 'milestone',
        color: 'amber',
        children: [
          { id: 'tl-3', label: t(language, 'startLifestyleChanges'), type: 'task' },
          { id: 'tl-4', label: t(language, 'monitorSymptoms'), type: 'task' }
        ]
      },
      {
        id: 'timeline-mid',
        label: t(language, 'months1to3'),
        type: 'milestone',
        color: 'blue',
        children: [
          { id: 'tl-5', label: t(language, 'followUpAppointment'), type: 'task' },
          { id: 'tl-6', label: t(language, 'retestIndicators'), type: 'task' }
        ]
      },
      {
        id: 'timeline-long',
        label: t(language, 'months3to6'),
        type: 'milestone',
        color: 'green',
        children: [
          { id: 'tl-7', label: t(language, 'comprehensiveReview'), type: 'task' },
          { id: 'tl-8', label: t(language, 'assessImprovement'), type: 'task' }
        ]
      }
    ]
  };

  nodes.children.push(timelineNode);

  // Add lifestyle node
  const lifestyleNode = {
    id: 'lifestyle',
    label: t(language, 'lifestyle'),
    type: 'lifestyle',
    children: [
      {
        id: 'ls-diet',
        label: t(language, 'dietNutrition'),
        type: 'lifestyle-area',
        color: 'green',
        children: [
          { id: 'ls-diet-1', label: t(language, 'balancedMeals'), type: 'task' },
          { id: 'ls-diet-2', label: t(language, 'reduceSugarSalt'), type: 'task' },
          { id: 'ls-diet-3', label: t(language, 'stayHydrated'), type: 'task' }
        ]
      },
      {
        id: 'ls-exercise',
        label: t(language, 'physicalActivity'),
        type: 'lifestyle-area',
        color: 'green',
        children: [
          { id: 'ls-ex-1', label: t(language, 'walkDaily'), type: 'task' },
          { id: 'ls-ex-2', label: t(language, 'stretching'), type: 'task' }
        ]
      },
      {
        id: 'ls-rest',
        label: t(language, 'restSleep'),
        type: 'lifestyle-area',
        color: 'green',
        children: [
          { id: 'ls-rest-1', label: t(language, 'sleepHours'), type: 'task' },
          { id: 'ls-rest-2', label: t(language, 'takeBreaks'), type: 'task' }
        ]
      },
      {
        id: 'ls-mental',
        label: t(language, 'mentalWellness'),
        type: 'lifestyle-area',
        color: 'green',
        children: [
          { id: 'ls-mental-1', label: t(language, 'stressReduction'), type: 'task' },
          { id: 'ls-mental-2', label: t(language, 'stayConnected'), type: 'task' }
        ]
      }
    ]
  };

  nodes.children.push(lifestyleNode);

  return {
    nodes,
    summary,
    disclaimer,
    profileInfo: profile ? {
      name: profile.fullName,
      age: profile.dateOfBirth ? calculateAge(profile.dateOfBirth) : null,
      nationality: profile.nationality,
      bloodType: profile.bloodType
    } : null,
    generatedAt: new Date().toISOString()
  };
}

function calculateAge(dob) {
  if (!dob) return null;
  const today = new Date();
  const birth = new Date(dob);
  let age = today.getFullYear() - birth.getFullYear();
  const m = today.getMonth() - birth.getMonth();
  if (m < 0 || (m === 0 && today.getDate() < birth.getDate())) age--;
  return age;
}

/**
 * Get color class for status
 */
export function getStatusColor(status) {
  switch (status) {
    case 'NORMAL': return '#10b981';
    case 'BORDERLINE': return '#f59e0b';
    case 'HIGH': return '#ef4444';
    case 'LOW': return '#3b82f6';
    default: return '#6b7280';
  }
}

/**
 * Get priority indicator
 */
export function getPriorityIndicator(priority) {
  switch (priority) {
    case 'high': return '!!!';
    case 'medium': return '!';
    case 'low': return '-';
    default: return '-';
  }
}